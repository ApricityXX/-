ans=0                            #定义计数变量，用于八个打印换行
for i in range(2000,2501):       #取头不取尾，2000-2500
    if(i%100==0 or i%4==0):      #判断是否闰年，整百或者除以4余数为零
        print(str(i)+"年",end=" ")  #以空格为分隔符,这里把i换成str类型，便于拼接
        ans+=1                   #ans每次加一
    if(ans%8==0):
        print()                  #如果ans对8取余为0，则说明输出了八个了，应该换行